/*----------------------------------------------------------
// 初期化する関数
----------------------------------------------------------*/

/**
* 技テーブルクリック時の関数
* @function click
* @return void
**/
var submitInit = function(){
    $('form[id^=first-pokemon-]').submit(function(){
        var name = $('#player-name').val();
        $(this).find('[name="name"]').val(name);
    });
}

/*----------------------------------------------------------
// 処理内で呼び出す関数
----------------------------------------------------------*/


/*----------------------------------------------------------
// 初期化
----------------------------------------------------------*/
jQuery(function($){
    submitInit();
});
